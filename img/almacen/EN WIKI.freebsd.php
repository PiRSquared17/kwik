El sistema operativo que prefiero para un PC-servidor, y el cual uso en el equipo que aloja esta página, es <a href="http://www.freebsd.org/es/">FreeBSD</a>.<img style="vertical-align: top" src="fhp_mini.png"><br />
A continuación ofrezco una serie de respuestas sobr /e él. Y en este enlace, todavia <a href="http://www.freebsd.org/doc/en_US.ISO8859-1/articles/explaining-bsd/article.html/">más respuestas</a>.
<h1>¿Por qué FreeBSD?</h1>

<ul>
  <li>porque está basado en UNIX: desde hace 30 años, UNIX es el sistema operativo de
  referencia a la hora de proporcionar sistemas sólidos. Internet es lo que es gracias a
  UNIX.
  </li><li>porque es rápido: incluso en modo gráfico funciona mejor que Windows. Equipos obsoletos
  <a href="http://www.onlamp.com/pub/a/bsd/2005/05/05/hardware_rescue.html">renacen</a> y proporcionan servicios, de fábr /ica, impensables con Windows (correo, webserver, ...)
  </li><li>porque es libr /e: podemos saber qué hace exactamente el sistema, por lo tanto es más seguro. Podemos modificar FreeBSD tanto como queramos.
  </li><li>porque ofrece una alternativa: Windows no es todo. Existe UNIX (entre otros). Y tampoco UNIX+Libr /e=Linux: software libr /e no significa
  Linux. Hay <a href="http://www.cptech.org/altos/">más alternativas</a> a Linux de las que pensamos. Si te gusta
  <a href="http://www.unix.org/">UNIX</a>, hay vida más allá de <a href="http://www.linux.org/">Linux</a>. Puedes leer una <a href="http://www.freebsd.org/doc/en_US.ISO8859-1/articles/linux-comparison/">comparativa con Linux</a>.
  Del mismo modo, si te gusta BSD, puedes probar <a href="http://www.openbsd.org/">OpenBSD</a> (muy seguro), <a href="http://www.netbsd.org/">NetBSD</a> (multiplataforma) o <a href="http://www.dragonflybsd.org/">DragonFlyBSD</a>.
  </li><li>porque es gratis: aunque normalmente usemos copias de Windows
  obtenidas 'de modo gratuito', es un sistema operativo por el que hay
  que pagar, y mucho.
  </li><li>porque es seguro: olvídate de virus y Windows Update. Tu sistema
  funcionará durante meses, no hace cosas 'solo' y no hay que reiniciar
  para aplicar una configuración. Hace lo que se supone que tiene que hacer
  mientras los demás pelean contra el Spyware. Además, la naturaleza libr /e del código es doble garantía de seguridad.
  </li><li>porque aprendes: cosas que con Windows ni soñabas que existían. Hay vida más
  alla del botón derecho del ratón.
  </li><li>porque merece ser utilizado: algo tan bueno no puede caer en el
  olvido, difúndelo. Por usar un PC no estás atado a Windows. Merece tanto ser utilizado que MacOS X, el mejor sistema operativo
  hoy por hoy, se basa en FreeBSD.
  </li><li>porque tiene más software del que esperas: un sistema operativo no es muy útil por sí mismo. En FreeBSD puedes usar <a href="http://www.freebsd.org/ports/">aplicaciones
  nativas</a>, aplicaciones Linux (por ejemplo <a href="http://java.sun.com/">Java</a>), e incluso aplicaciones MS-DOS y Windows gracias a <a href="http://dosbox.sourceforge.net/">DOSBox</a> y <a href="http://www.winehq.com">Wine</a>.
  Dicho sea de paso, puedes encontrar juegos 'abandonados' en <a href="http://www.the-underdogs.org/">Home Of The Underdogs</a> y en <a href="http://www.reloaded.org/">Abandonia</a>, por citar dos sitios.
  </li><li>porque la mascota es muy maja: esto es plenamente
  subjetivo, pero quería ponerlo... http://www.freebsdmall.com/
  </li></ul>
  <h1>¿Dónde obtener FreeBSD?</h1>
  El camino más directo es bajarse una imagen de CD de <a href="http://www.freebsd.org/es/where.html">la web</a>, tostarla, meterla en la unidad y reiniciar.
  <h1>El programa de instalación me parece complicado, ¿no lo hay en modo gráfico?</h1>
  Sí, y el sistema instalado sigue siendo FreeBSD. Existen <a href="http://www.pcbsd.com/">PC-BSD</a> y <a href="http://www.freesbie.org/">Freesbie</a>.
  De todos modos, un poco de modo texto no hace daño a nadie, sirvan <a href="http://www.freebsd.bishopston.net/hour_install.html">estas instrucciones</a> de ejemplo.
